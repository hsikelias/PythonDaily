#1. String Slicing — First 4 letters
word = "Mango"
# Print the first 4 letters
print(word[0:4])

#2. Using len() — Word Length
fruit = "Pineapple"
len1 = len(fruit)
print("Pineapple is a", len1, "letter word.")

#3 upper() and lower()

msg = "Good Morning"
# Print it in all caps, and then all lowercase.
print(msg.upper())
print(msg.lower())

#4 replace()
sentence = "I love apples"
# Replace "apples" with "mangoes"
print(sentence.replace("apples","mangoes"))

#5 count()
line = "banana banana banana"
# Count how many times "banana" appears
print(line.count("banana"))

#6 Negative Slicing
text = "Programming"
print(text.split[:10])





